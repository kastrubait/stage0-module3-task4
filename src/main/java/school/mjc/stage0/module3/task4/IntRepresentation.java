package school.mjc.stage0.module3.task4;

public class IntRepresentation {
    public static void main(String[] args) {
        int code = 99;
        char c = (char) code;
        System.out.println(c);

        code = 104;
        char h = (char) code;
        System.out.println(h);

        code = 97;
        char a = (char) code;
        System.out.println(a);

        code = 114;
        char r = (char) code;
        System.out.println(r);
    }
}
